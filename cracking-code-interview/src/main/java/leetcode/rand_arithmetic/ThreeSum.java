  static class Solution {
    public List<List<Integer>> threeSum(int[] nums) {
      var results = new HashSet<List<Integer>>();
      if (nums.length < 3) {
        return new ArrayList<>(results);
      }

      if (Arrays.stream(nums).boxed().allMatch(n -> n == 0)) {
        results.add(List.of(0, 0, 0));
        return new ArrayList<>(results);
      }

      Integer[][] twoSums = new Integer[nums.length][nums.length];
      for (int i = 0; i < nums.length; ++i) {
        for (int j = i + 1; j < nums.length; ++j) {
          twoSums[i][j] = nums[i] + nums[j];
        }
      }

      var indices = new HashMap<Integer, HashSet<Integer>>();
      for (int i = 0, numsLength = nums.length; i < numsLength; i++) {
        indices.computeIfAbsent(nums[i], key -> new HashSet<>()).add(i);
      }

      int target = 0;
      for (int i = 0; i < nums.length; ++i) {
        for (int j = i + 1; j < nums.length; ++j) {
          var rem = target - twoSums[i][j];
          var remIndices = indices.get(rem);
          if (remIndices == null) continue;
          var result = new ArrayList<>(List.of(nums[i], nums[j], rem));
          Collections.sort(result);
          if (results.contains(result)) continue;
          var iRemoved = remIndices.remove(i);
          var jRemoved = remIndices.remove(j);
          if (!remIndices.isEmpty()) {
            results.add(result);
          }
          if (iRemoved) remIndices.add(i);
          if (jRemoved) remIndices.add(j);
        }
      }

      return new ArrayList<>(results);
    }
  }

